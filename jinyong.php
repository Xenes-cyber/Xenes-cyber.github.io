          <td class="vcss">　　金庸（1924年农历2月初6 ――）。原名查良镛(zhā liáng yōng，英：Louis Cha)，当代著名作家、新闻学家、企业家、政治评论家、社会活动家，《香港基本法》主要起草人之一、香港“大紫荆勋章” 获得者、华人作家首富。金庸是新派武侠小说最杰出的代表作家，被普遍誉为武侠小说史上前无古人后无来者的“绝代宗师”和“泰山北斗”，更有金迷们尊称其为“金大侠”或“查大侠”。<br />
            　　 金庸祖籍为江西省婺源县，1924年出生在浙江海宁。查家为当地名门望族，有“唐宋以来巨族，江南有数人家”之誉。历史上查家最鼎盛期为清康熙年间，以查慎行为首叔侄七人同任翰林，有“一门七进士，叔侄两翰林”之说，后全族人分入汉族八旗，正式成为包衣。现代查氏家族还有两位知名人物，南开大学教授查良铮（穆旦）（四十年代九叶派代表诗人，翻译家），台湾学术界风云人物、司法部长查良钊。出自海宁的著名人物还有王国维和徐志摩。徐志摩是金庸的表兄。其先祖查继佐是“明史案”最早向当局告发人之一。金庸祖父查沧珊是“丹阳教案”的当事人。 <br />
            　　 当年金庸与梁羽生订下武侠小说之约时，曾想过笔名的问题。后来决定把自己名字的“镛”字一分为二，成为“金庸”。这就成为了他的笔名。<br />
            　　 金庸先生也用过其他的笔名，例如用姚馥兰(英文“你的朋友[Your Friend」”音译)写影话，如用林欢这笔名写影话也写电影剧本，在读者中间也曾留下较深刻的印象。</td>
